let minhaVariavelBonita;
// Existem vários valores possíveis que podemos atribuir a uma variável
// E esses valores podem ser classificados em tipos diferentes
// Começaremos com os tipos primitivos:
// string       -> texto
// number       -> número
// boolean      -> verdadeiro ou falso (true, false)
// null         -> nulo
// undefined    -> valor específico do JS, significa não definido

console.log(minhaVariavelBonita);
// Demonstrar os tipos primitivos:
// string
minhaVariavelBonita = "meu texto legal";
//minhaVariavelBonita = 'meu texto legal';
minhaVariavelBonita = `meu texto legal 2`;
console.log(minhaVariavelBonita);
// number
minhaVariavelBonita = 200;
console.log(minhaVariavelBonita);
minhaVariavelBonita = 200.2;
console.log(minhaVariavelBonita);
// boolean
minhaVariavelBonita = false;
console.log(minhaVariavelBonita);
minhaVariavelBonita = true;
console.log(minhaVariavelBonita);
// null
minhaVariavelBonita = null;
console.log(minhaVariavelBonita);
// undefined
minhaVariavelBonita = undefined;
console.log(minhaVariavelBonita);
let novaVariavel;
console.log(novaVariavel);
