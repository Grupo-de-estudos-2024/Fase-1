// Exercício Arrays 01

let myArray = ['javascript', 'C sharp', 'Python']
console.log('meu array inicial', myArray)

// Alterando os dois primeiros elementos usando sintaxe de colchetes
myArray[0] = 'Java'
myArray[1] = 'C++'
console.log('meu array alterado', myArray)

// Adicionando elemento ao começo do array
myArray.unshift('C')
console.log('meu array com elemento adicionado ao começo', myArray)
